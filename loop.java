import java.util.Scanner;

public class loop {
    public static void main(String[] args) {
        //for loop
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
        }
        System.out.println();


//while loop
        int j = 10;
        while (j > 0) {
            System.out.print(j);
            j--;
        }
        System.out.println();



        //do-while
        int i=1;
        do {
            System.out.print(i);
            i++;
        }while(i<=10);

    }


}
